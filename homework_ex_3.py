# 6. Дано число N. Найти произведение всех чисел от 0 до N.
print("Задание №6")
print("*" * 40)

N = int(input("Введите целое число N: "))

print(f"Задача: найти произведение всех чисел от 0 до {N}, но факториал 0!=1")
print("^" * 20)

if N < 0:
    print("Ошибка: N должно быть положительное число")
elif N == 0:
    print("Произведение чисел от 1 до 0 = 1 (пустое произведение)")
else:
    product = 1
    # Цикл для умножения чисел от 1 до N, так 0! принимается за 1 (поэтому product объявлена c 1)
    for i in range(1, N + 1):
        product *= i
        print(f"Шаг {i}: умножаем на {i}, получаем {product}")

    print(f"\nПроизведение чисел от 1 до {N} = {product}")
    print(f"(Это факториал числа {N}: {N}! = {product})")
print("*" * 40)

# 7. Поле засеяли цветами двух сортов на площади S1 и S2. Каждый год площадь цветов первого сорта увеличивается вдвое, а площадь второго сорта
# увеличивается втрое. Через сколько лет площадь первых сортов будет составлять меньше 10% от площади вторых сортов.
print("Задание №7")
print("*" * 40)

S1 = float(input("Введите площадь цветов первого сорта (S1): "))
S2 = float(input("Введите площадь цветов второго сорта (S2): "))

if S1 <= 0 or S2 <= 0:
    print("Ошибка: площади должны быть положительными числами")
else:
    year = 0
    while S1 >= 0.1 * S2:
        S1 = S1 * 2
        S2 = S2 * 3
        year += 1

    print(f"\nОтвет: через {year} лет")
    print(f"Площадь первых сортов: {S1:.2f}")
    print(f"Площадь вторых сортов: {S2:.2f}")
    print(f"Соотношение: {S1 / S2 * 100:.2f}%")

print("*" * 40)

