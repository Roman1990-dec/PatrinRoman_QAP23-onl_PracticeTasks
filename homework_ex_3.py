print("*" * 40)

A = int(input("Введите начало диапазона A: "))
B = int(input("Введите конец диапазона B: "))

# Проверим, что у нас был задан валидный диапазон:
# - начало диапазона должно быть меньше или равно концу
# - натуральные числа только положительные и не 0
if A <= B and A > 0:
    summa = 0
    for i in range(A, B + 1):
        summa += i
    print(f"Сумма чисел от {A} до {B} = {summa}")
else:
    print("Ошибка: диапазон задан некорректно!")

print("*" * 40)

