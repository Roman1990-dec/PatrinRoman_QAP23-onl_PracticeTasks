# 3. Дан номер года (положительное целое число). Определить количество дней в этом году, учитывая, что обычный год насчитывает 365 дней, а високосный — 366
# дней. Високосным считается год, делящийся на 4, за исключением тех годов, которые делятся на 100 и не делятся на 400 (например, годы 300, 1300 и 1900 не являются
# високосными, а 1200 и 2000 являются).
print("Задание №3")
print("*" * 40)

year = int(input("Введите год: "))

# Проверка на високосность
if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
    days = 366
    print(f"Год {year} високосный")
else:
    days = 365
    print(f"Год {year} не високосный")

print(f"Количество дней в {year} году: {days}")
print("*" * 40)

# 4. Найти сумму всех натуральных чисел в от A до B
